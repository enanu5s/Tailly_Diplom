const SERVICE_TITLES = ['Выгул', 'Передержка', 'Груминг', 'Тренировка', 'Фотосессия'] as const;

function pad2(n: number): string {
  return String(n).padStart(2, '0');
}

export function buildSeedHomeReviews(): Review[] {
  const now = Date.now();

  return REVIEW_BODIES.map((text, i) => {
    const client = CLIENT_NAMES[i % CLIENT_NAMES.length]!;
    const specialist = SPECIALIST_ACCOUNT_META[i % SPECIALIST_ACCOUNT_META.length]!;
    const orderId = `home-review-order-${i + 1}`;
    const daysAgo = i + 1;

    return {
      id: `home-review-${i + 1}`,
      orderId,
      rating: 5,
      text,
      photoUrls: [
        `/images/home-reviews/hr-${pad2(i + 1)}.jpg`,
        ...(i % 2 === 0 ? [`/images/home-reviews/hr-${pad2(i + 1)}-2.jpg`] : []),
      ],
      createdAtIso: new Date(now - daysAgo * 86_400_000).toISOString(),
      petName: ['Марта', 'Пушок', 'Барсик', 'Рыжик', 'Снежок', 'Шарик', 'Мурзик', 'Бобик'][i]!,
      ownerFullName: `${client.firstName} ${client.lastName}`,
      sitterId: specialist.id,
      sitterName: `${specialist.firstName} ${specialist.lastName}`,
      serviceTitle: SERVICE_TITLES[i % SERVICE_TITLES.length]!,
    };
  });
}
